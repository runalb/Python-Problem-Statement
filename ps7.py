a=float(input("Enter no: "))
b=float(input("Enter no: "))
c=input("Enter Operator: ")

if(c=="+"):
    print(a+b)
elif(c=="-"):
    print(a-b)
elif(c=="/"):
    print(a/b)
elif(c=="%"):
    print(a%b)
elif(c=="*"):
    print(a*b)
elif(c=="//"):
    print(a//b)
elif(c=="**"):
    print(a**b)